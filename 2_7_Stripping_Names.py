my_name = "\tRico Robinson\n"

print("Regular:")
print(my_name)

print("\nUsing lstrip():")
print(my_name.lstrip())

print("\nUsing rstrip():")
print(my_name.rstrip())


print("\nUsing strip():")
print(my_name.strip())

